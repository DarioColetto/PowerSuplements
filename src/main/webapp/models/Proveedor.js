export class Proveedor {


    id_proveedor;
    nombre;
    telefono;
    direccion;
    email;

    constructor(id_proveedor, nombre, telefono ,direccion, email){

        this.id_proveedor = id_proveedor;
        this.nombre = nombre;
        this.telefono =telefono;
        this.direccion =direccion;
        this.email = email;

    }


}